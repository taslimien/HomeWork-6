package stringhomework;

public class StringMethods {

	public static void main(String[] args) {
		
		String name = "My name is Mohammad Taslim Uddin, I want to go JAVA Island.";
		
		System.out.println("First Charecter is: " + name.charAt(0));
		System.out.println("Last Charecter is: " + name.charAt(57));
		System.out.println("Substring: "+ name.substring(11, 19));
		System.out.println("Name: " + name);
		System.out.println("Upper Case: " + name.toUpperCase());
		System.out.println("Lower Case: " + name.toLowerCase());
		System.out.println("Length is: " + name.length());
		
		

	}

}
